         <h6 class="black-text" style="line-height: 21px;">&emsp;&emsp;&emsp;&emsp;Ao redigir um texto, especialmente no caso de textos mais objetivos, como é o caso  de dissertações escolares, textos acadêmicos etc., é importante organizar os parágrafos destes de maneira lógica. Nesse sentido, a progressão textual é fundamental, pois permite estabelecer relação entre cada um dos parágrafos anteriores àqueles que ainda serão lidos.</br>
          &emsp;&emsp;&emsp;&emsp;Ao organizar uma sequência de ideias, cada parágrafo deve ser estruturado de maneira a dialogar com um parágrafo escrito anteriormente. Além disso, é importante observar os parágrafos posteriores a estes para que cada parágrafo no texto articule-se um ao outro num processo progressivo, seguindo lógica em relação ao que foi e ainda não foi dito para que o texto faça sentido ao leitor.</br>
          &emsp;&emsp;&emsp;&emsp;Esse processo está muito mais além da estrutura de introdução, desenvolvimento e conclusão. A progressão também é mais específica e, não necessariamente, articula-se a esta ordem, porque nem todos os textos seguem-na (textos informativos, por exemplo, não necessariamente precisam segui-la). A progressão textual está para a lógica de cada uma das orações, está para o desencadeamento de ideias, para os “ganchos” que
          se estabelece no texto, sejam eles relacionados ao conteúdo em si ou a marcadores gramaticais.
        </br>
